using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Capa_logica
{
    //clase proceso//
    public class PROCESO
    {
        //declaracion variables//

        public double d1, d2, d3;
        
        //funcion suma//
        public void prosuma()
        {
            d3 = d1 + d2;
        }

        //funcion resta//
        public void proresta()
        {
            d3 = d1 - d2;
        }

        //funcion multiplicacion//
        public void promultiplicacion()
        {
            d3 = d1 * d2;
        }

        //funcion division//
        public void prodivision()
        {
            d3 = d1 / d2;
        }
         
             
    }
}
